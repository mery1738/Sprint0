    private void mostrarInformacionDispositivoBTLE(ScanResult resultado) {
        if (resultado == null) return;

        BluetoothDevice bluetoothDevice = resultado.getDevice();
        int rssi = resultado.getRssi();

        Log.d(ETIQUETA_LOG, " ****************************************************");
        Log.d(ETIQUETA_LOG, " ****** DISPOSITIVO DETECTADO BTLE ****************** ");
        if (ActivityCompat.checkSelfPermission(this, Manifest.permission.BLUETOOTH_CONNECT) != PackageManager.PERMISSION_GRANTED) {
            // TODO: Consider calling
            //    ActivityCompat#requestPermissions
            // here to request the missing permissions, and then overriding
            //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
            //                                          int[] grantResults)
            // to handle the case where the user grants the permission. See the documentation
            // for ActivityCompat#requestPermissions for more details.
            return;
        }
        Log.d(ETIQUETA_LOG, " nombre = " + bluetoothDevice.getName());
        Log.d(ETIQUETA_LOG, " dirección = " + bluetoothDevice.getAddress());
        Log.d(ETIQUETA_LOG, " rssi = " + rssi);

        // ---- check ScanRecord safely ----
        if (resultado.getScanRecord() == null || resultado.getScanRecord().getBytes() == null) {
            Log.d(ETIQUETA_LOG, " scanRecord is null");
            return;
        }
        byte[] bytes = resultado.getScanRecord().getBytes();
        Log.d(ETIQUETA_LOG, " raw bytes (" + bytes.length + ") = " + Utilidades.bytesToHexString(bytes));

        // ---- look for iBeacon signature ----
        String hex = Utilidades.bytesToHexString(bytes);
        if (hex.contains("4C000215")) {   // Apple prefix for iBeacon
            try {
                TramaIBeacon tib = new TramaIBeacon(bytes);
                Log.d(ETIQUETA_LOG, " ---- Detected iBeacon ----");
                Log.d(ETIQUETA_LOG, " uuid  = " + Utilidades.bytesToString(tib.getUUID()));
                Log.d(ETIQUETA_LOG, " major = " + Utilidades.bytesToInt(tib.getMajor()));
                Log.d(ETIQUETA_LOG, " minor = " + Utilidades.bytesToInt(tib.getMinor()));
                Log.d(ETIQUETA_LOG, " txPower = " + tib.getTxPower());
            } catch (Exception e) {
                Log.d(ETIQUETA_LOG, " Error parsing iBeacon: " + e.getMessage());
            }
        } else {
            Log.d(ETIQUETA_LOG, " Not an iBeacon packet (skipping TramaIBeacon)");
        }

        Log.d(ETIQUETA_LOG, " ****************************************************");
    }


